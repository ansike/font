package com.fd.font.cloud.font.dto;

public class FontDetailDto {

	
	private String code;
	private String name;
	private String introduction;
	private String author;
	private String versionName;
	private String ttfDownloadUrl;
	private Integer width;
	private Integer height;
	private String picUrl;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIntroduction() {
		return introduction;
	}
	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getVersionName() {
		return versionName;
	}
	public void setVersionName(String versionName) {
		this.versionName = versionName;
	}
	public String getTtfDownloadUrl() {
		return ttfDownloadUrl;
	}
	public void setTtfDownloadUrl(String ttfDownloadUrl) {
		this.ttfDownloadUrl = ttfDownloadUrl;
	}
	public Integer getWidth() {
		return width;
	}
	public void setWidth(Integer width) {
		this.width = width;
	}
	public Integer getHeight() {
		return height;
	}
	public void setHeight(Integer height) {
		this.height = height;
	}
	public String getPicUrl() {
		return picUrl;
	}
	public void setPicUrl(String picUrl) {
		this.picUrl = picUrl;
	}
	
	
}
